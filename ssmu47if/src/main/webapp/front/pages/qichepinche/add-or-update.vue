	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				qichemingcheng: '',
				qicheleixing: '',
				qichepinpai: '',
				tupian: '',
				yanse: '',
				chepai: '',
				pinchejiage: '',
				pincherenshu: '',
				pincheneirong: '',
				fabushijian: '',
				},
				qicheleixingOptions: [],
				qicheleixingIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   qichemingcheng : false,
                                   qicheleixing : false,
                                   qichepinpai : false,
                                   tupian : false,
                                   yanse : false,
                                   chepai : false,
                                   pinchejiage : false,
                                   pincherenshu : false,
                                   pincheneirong : false,
                                   fabushijian : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
        		this.ruleForm.fabushijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

			// 自定义下拉框值
			this.qicheleixingOptions = "小轿车,SUV,跑车,越野车".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`qichepinche`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='qichemingcheng'){
					this.ruleForm.qichemingcheng = obj[o];
					this.ro.qichemingcheng = true;
					continue;
					}
					if(o=='qicheleixing'){
					this.ruleForm.qicheleixing = obj[o];
					this.ro.qicheleixing = true;
					continue;
					}
					if(o=='qichepinpai'){
					this.ruleForm.qichepinpai = obj[o];
					this.ro.qichepinpai = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='yanse'){
					this.ruleForm.yanse = obj[o];
					this.ro.yanse = true;
					continue;
					}
					if(o=='chepai'){
					this.ruleForm.chepai = obj[o];
					this.ro.chepai = true;
					continue;
					}
					if(o=='pinchejiage'){
					this.ruleForm.pinchejiage = obj[o];
					this.ro.pinchejiage = true;
					continue;
					}
					if(o=='pincherenshu'){
					this.ruleForm.pincherenshu = obj[o];
					this.ro.pincherenshu = true;
					continue;
					}
					if(o=='pincheneirong'){
					this.ruleForm.pincheneirong = obj[o];
					this.ro.pincheneirong = true;
					continue;
					}
					if(o=='fabushijian'){
					this.ruleForm.fabushijian = obj[o];
					this.ro.fabushijian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数


			// 日长控件选择日期时间
			fabushijianConfirm(val) {
				console.log(val)
				this.ruleForm.fabushijian = val.result;
				this.$forceUpdate();
			},

			// 下拉变化
			qicheleixingChange(e) {
				this.qicheleixingIndex = e.target.value
				this.ruleForm.qicheleixing = this.qicheleixingOptions[this.qicheleixingIndex]
			},

			tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if((!this.ruleForm.pinchejiage)){
					this.$utils.msg(`拼车价格不能为空`);
					return
				}
				if(this.ruleForm.pinchejiage&&(!this.$validate.isIntNumer(this.ruleForm.pinchejiage))){
					this.$utils.msg(`拼车价格应输入整数`);
					return
				}
				if((!this.ruleForm.pincherenshu)){
					this.$utils.msg(`拼车人数不能为空`);
					return
				}
				if(this.ruleForm.pincherenshu&&(!this.$validate.isIntNumer(this.ruleForm.pincherenshu))){
					this.$utils.msg(`拼车人数应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`qichepinche`, this.ruleForm);
				}else{
					await this.$api.add(`qichepinche`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

